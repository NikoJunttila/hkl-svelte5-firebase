<p class="font-bold">Yksityisyys ja tietosuoja</p>
<p>Hyvinvointikeskus Luxuksen asiakastiedot ovat luottamuksellisia.
Asiakkaiden tietoja (nimi, osoite, puhelin numero, sähköpostiosoite) luovutetaan Hyvinvointikeskus
Luxukselle, jonka tarkoituksena on laskujen ja palvelujen toimittaminen asiakkaille.
Asiakkaiden henkilötietoja käsitellessään Susanna Höijer noudattaa henkilötietolakia, lakia yksityisyyden
suojasta, tietoturvasta sekä muuta tietosuojaan liittyvää lainsäädäntöä ja hyvää tietojenkäsittelytapaa.</p>
<br>
<h5 class="font-bold">Rekisterin sisältämät tiedot</h5>
<p>Henkilörekisteri sisältää seuraavia tietoja:</p>
<p>a) Etunimi, sukunimi, laskutusosoite, sähköpostiosoite, puhelinnumero</p>
<p>b) Etunimi, sukunimi, yritys, sähköpostiosoite, laskutusosoite, puhelinnumero
Tietoja ei luovuteta eteenpäin.</p>
<br>
<p>Tietojen säilytys</p>
<p>Tietoja säilytetään sähköisessä muodossa ja siihen on käyttöoikeus vain rekisterin pitäjällä.
Asiakkaiden tietoja säilytetään vain sen ajan, kuin se on asianmukaista eikä henkilötietoja säilytetä
pidempään kuin on tarpeen.</p>
<p>Sähköpostiarkistoa ei ole muualla kuin hyvinvointipalveluissa ja verkkokaupassa, asiakkaille jotka ovat varanneet nettiajanvarauksen kautta aikoja ja asiakkaille jotka ovat halunneet rekisteröityä asiakasrekisteriin.</p>
